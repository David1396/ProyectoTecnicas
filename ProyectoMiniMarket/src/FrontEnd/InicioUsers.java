    public boolean Ingreso(String usuario,String pass,int seleccion){
        boolean ingres = false; 
        String cedula = null;
        String contrasena = null;
        if(seleccion == 0){
            System.out.println("Entra1");
            ResultSet res = Conexiones.Conexion.Consulta("select * from USUARIO");
            try{
                while(res.next()){
                    cedula = res.getString("NOMBREUSUARIO");
                    contrasena = res.getString("CONTRASENA");
                    
                    if(cedula.equals(usuario) && contrasena.equals(pass)){
                       ingres = true;
                    }
                }
            }catch (SQLException e){
            }
        }
        return ingres;
    }
